<H2><center>New Jersey General Assembly and Senate</center></H2>

<br>
Every New Jersey legislative district is represented in the State Assembly by 2 Representatives and in the State Senate by 1 Senator.
<BR>
There are 40 Legislative Districts in NJ with roughly the same population each based on the last decennial census.
For the 2021 elections, the districts have not been redesigned yet.

